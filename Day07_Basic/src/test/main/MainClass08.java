package test.main;
/*
 * switch ~ case
 */
public class MainClass08 {
	public static void main(String[] args) {
		char ch='b';
		
		switch(ch){
		case 'a':
			System.out.println("a 입니다.");
			break;
		case 'b':
			System.out.println("b 입니다.");
			break;
		case 'c':
			System.out.println("c 입니다.");
			break;
		default :
			System.out.println("a,b,c 가 아닙니다.");
			break;
		}
	}
}





